#include "fractol.h"

int	discont_to_black(int col)
{
	int	r;
	int	g;
	int	b;

	r = col & 0xFF;
	g = col >> 8 & 0xFF;
	b = col >> 16 & 0xFF;
	if (r == 255 && g == 0 && b == 0)
		return (0);
	return (col);
}
